#!/usr/bin/env perl

use v5.14;

BEGIN { $ENV{PERL_JSON_BACKEND} = 0; } # Force JSON::PP.

use strict;
use warnings;
use File::Slurp;
use JSON;
use JSON::Tiny;
use Benchmark qw( cmpthese );

my @json = do { split /-{5,}/, read_file(\*DATA); };

sub json_pp {
    my $j = JSON->new->relaxed;
    my @decoded = map { $j->decode( $_ ) } @json;
    return \@decoded;
}

sub json_tiny {
    my $j = JSON::Tiny->new;
    my @decoded = map { $j->decode( $_ ) } @json;
    return \@decoded;
}

cmpthese ( -15, {
    JSON_PP   => \&json_pp,
    JSON_Tiny => \&json_tiny,
} );



